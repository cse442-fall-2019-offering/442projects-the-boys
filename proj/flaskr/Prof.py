
class Prof:
    def __init__(self, name, age, gender, salary, email, jobTitle, setting, total):
        self.name = name
        self.age = age
        self.gender = gender
        self.salary = salary
        self.email = email
        self.jobTitle = jobTitle
        self.setting = setting
        self.total = total


    def setName(self, n):
        self.name = n

    def getName(self):
        return self.name

    def setAge(self, a):
        self.age = a

    def getAge(self):
        return self.age

    def setGender(self, g):
        self.age = g

    def getGender(self):
        return self.gender

    def setSalary(self, s):
        self.salary = s

    def getSalary(self):
        return self.salary

    def setEmail(self, e):
        self.email = e

    def getEmail(self):
        return self.email

    def setJobTitle(self, j):
        self.jobTitle = j

    def getJobTitle(self):
        return self.jobTitle

    def getTotal(self):
        return self.total

    def setTotal(self, t):
        self.total = t





